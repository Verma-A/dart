void main(){

  //for loop
  print('Count from 1-5 using for loop');
  for (int i=1;i<6;i++){
    print(i);
  }

  //while
  print('Count from 1-5 using while loop');
  int a=1;
  while(a<6){
    print(a);
    a++;
  }

  //do while
  print('Count from 1-5 using do-while loop');
  a=1;
  do{
    print(a);
    a++;
  }while(a<6);
}